import pytest
from server.services.text_processing_service import clean_text

# נניח שהפונקציה שלך נקראת clean_text ומיובאת
# from your_module import clean_text

def test_clean_text():
    test_cases = [
        ("שלום!!! מה שלומך???", "שלום מה שלומך"),
        ("   הרבה     רווחים     כאן   ", "הרבה רווחים כאן"),
        ("שורה ראשונה\n\nשורה שנייה\n\n\nשורה שלישית", "שורה ראשונה שורה שנייה שורה שלישית"),
        ("שלום 😊 איך אתה 😎 היום!!!", "שלום איך אתה היום"),
        ("זה טקסט: עם; הרבה, סימנים!!!???", "זה טקסט עם הרבה סימנים"),
        ("  😄 שלום!!!   \n\n איך    הולך???   © ™ ", "שלום איך הולך"),
        ("""
        זהו טקסט גדול מאוד!!!

        יש בו שורות ריקות,     הרבה רווחים, 😊 אמוג'ים, וסימנים!!!

        המטרה היא לנקות אותו לגמרי.
        """, "זהו טקסט גדול מאוד יש בו שורות ריקות הרבה רווחים אמוגים וסימנים המטרה היא לנקות אותו לגמרי")
    ]

    for input_text, expected in test_cases:
        assert clean_text(input_text) == expected
